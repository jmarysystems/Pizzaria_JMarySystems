    private void addAListResolvidos( int n ) {   
        
        if( !listN.isEmpty() ){
            for (int i = 0; i < listN.size(); i++){
                if( n == listN.get(i) ){
                    System.out.println( " break " );
                    break;
                }
                else{
                    int ci = i + 1;
                    int cl = listN.size();
                    if( ci == cl ) {  
                        System.out.println( " N " + n );
                        listN.add(n);
                    }
                }
            }
        }
        else{
            listN.add(n);
        }
        
    }
      
