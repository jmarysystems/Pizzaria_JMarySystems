    private void btRespostaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btRespostaActionPerformed
        new Thread() {   @Override public void run() { try { Thread.sleep( 1 ); 
                
             if( getBookInfoExercicios().RESPOSTACERTA.equals( letraResposta ) ){
                 int n = Integer.parseInt( tfAcertos.getText().trim() );
                 n+=1;
                 tfAcertos.setText( String.valueOf( n ) );
                 evtRbs();
                 if( rbProxAuto.isSelected() ){
                     addAListResolvidos( BookInfoExercicios.ID );
                     proximoExercicio();
                 }
             }
             else{ //tfErros
                 int n = Integer.parseInt( tfErros.getText().trim() );
                 n+=1;
                 tfErros.setText( String.valueOf( n ) );
                 evtRbs();
                 if( rbProxAuto.isSelected() ){
                     addAListResolvidos( BookInfoExercicios.ID );
                     proximoExercicio();
                 }
             }
            
        } catch( Exception e ){ JOPM JOptionPaneMod = new JOPM( 2, "btRespostaActionPerformed, \n"
                + e.getMessage() + "\n", this.getClass().getSimpleName() ); } } }.start();  
       
    }//GEN-LAST:event_btRespostaActionPerformed

