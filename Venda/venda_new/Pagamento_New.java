    private void btEncerrarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btEncerrarMousePressed
        if( !jTextField1.getText().trim().equals("") ) {
            
            float vPago = Float.parseFloat( jTextField1.getText().trim() );
            if( vPago >= Venda_New.vTotal ){
                
                jTextField1.setText( String.valueOf( vPago - Venda_New.vTotal ) );
                
                btEncerrar.setVisible(false);
                jLabel1.setText("TROCO");
                lbValor.setText("VENDA ENCERRADA");
                lbValor.setVisible(true);
                
                Venda_New.btSalvar.setVisible(false);
                Venda_New.lbEncerra.setText("VENDA ENCERRADA");
                Venda_New.salvarXX();
            }
            else{
                lbValor.setText("VALOR PAGO INV√ÅLIDO");
                lbValor.setVisible(true);
            }

        }
    }//GEN-LAST:event_btEncerrarMousePressed


